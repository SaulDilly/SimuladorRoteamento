public class IntervaloTempo {
    
    private int limInicial;
    private int limFinal;

    public IntervaloTempo(int limInicial, int limFinal) {
        this.limInicial = limInicial;
        this.limFinal = limFinal;
    }

    public int getLimInicial() {
        return limInicial;
    }

    public int getLimFinal() {
        return limFinal;
    }

}
